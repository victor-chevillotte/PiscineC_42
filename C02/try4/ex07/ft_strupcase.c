char	*ft_strupcase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 122)
		{
			str[i] -= 32;
		}
		i++;
	}
	return (str);
}
